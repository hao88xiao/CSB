package com.linkage.bss.crm.intf.model;

import java.io.Serializable;
import java.util.List;

public class ServParam implements Serializable {

	private static final long serialVersionUID = 1L;
	private Long id;
	private String name;
	private String value;
	private String defaultSpecValue;
	private List<DiscreateValue> discreateValues;
	private String ifNullAble;

	public String getDefaultSpecValue() {
		return defaultSpecValue;
	}

	public void setDefaultSpecValue(String defaultSpecValue) {
		this.defaultSpecValue = defaultSpecValue;
	}

	public String getIfNullAble() {
		return ifNullAble;
	}

	public void setIfNullAble(String ifNullAble) {
		this.ifNullAble = ifNullAble;
	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}

	public List<DiscreateValue> getDiscreateValues() {
		return discreateValues;
	}

	public void setDiscreateValues(List<DiscreateValue> discreateValues) {
		this.discreateValues = discreateValues;
	}
}
